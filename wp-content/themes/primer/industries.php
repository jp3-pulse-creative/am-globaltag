function getURLParameter(name) {
  return decodeURI(
   (RegExp(name + '=' + '(.+?)(&|$)').exec(location.search)||[,null])[1]
   );
}
$( document ).ready(function() {
    $(".industry-title").click(function(e){
    	e.preventDefault();
    	if(!$(this).parent().hasClass("active")){
    		$(".industry-block").removeClass("active");
    		$(this).parent().addClass("active");
    	}else{
    		$(".industry-block").removeClass("active");
    	}
    });
	var ind = getURLParameter('ind');
	if(ind){
		var ind_elem = document.getElementById(ind);
		$(ind_elem).addClass("active");
		ind_elem.scrollIntoView();
	}
});
</script>
