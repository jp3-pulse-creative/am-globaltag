			$(this).addClass('active');
			serv_index = parseInt($(this).index());
			console.log(serv_index);
			$(".services-slider").slick('slickGoTo', serv_index * 3);
		});
		$(".services-nav .s-left").click(function () {
			$(".services-btns .slide-ind li").removeClass('active');
			serv_index -= 1;
			if (serv_index < 0) {
				serv_index = 3;
			}
			$(".services-btns .slide-ind li").eq(serv_index).addClass('active');
			$(".services-slider").slick('slickPrev');
		});
		$(".services-nav .s-right").click(function () {
			$(".services-btns .slide-ind li").removeClass('active');
			serv_index += 1;
			if (serv_index > 3) {
				serv_index = 0;
			}
			$(".services-btns .slide-ind li").eq(serv_index).addClass('active');
			$(".services-slider").slick('slickNext');
		});
		$(".pieslice").hover(function () {
