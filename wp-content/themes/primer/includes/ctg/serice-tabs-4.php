<script>
    $(document).ready(function() {
        $('.tab').click(function() {
            var id = $(this).attr('id');
            if ($(this).hasClass('open')) {
                // If the clicked tab is already open, remove 'open' class from it and 'fade-blue' class from all tabs
                $(this).removeClass('open');
                $('.tab').removeClass('fade-blue');
                // Also remove 'open' class from all panels
                $('.tab-panel').removeClass('open');
            } else {
                // If the clicked tab is not open, remove 'open' and 'fade-blue' classes from all tabs
                $('.tab').removeClass('open fade-blue');
                // Add 'open' class to the clicked tab and 'fade-blue' class to other tabs
                $(this).addClass('open');
                $('.tab').not('#' + id).addClass('fade-blue');
                // Remove 'open' class from all panels
                $('.tab-panel').not('[data-tab-panel="' + id + '"]').removeClass('open');
                // Add 'open' class to the matching panel
                $('[data-tab-panel="' + id + '"]').addClass('open');
            }
        });
    });
</script>